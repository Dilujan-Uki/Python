# Create list
motorbike = ['honda', 'yamaha', 'suzuki', 'bajaj'];
print(motorbike);
# Replace item
motorbike[0] = 'ducati';
print(motorbike);
# Item add in last
motorbike.append('honda');
print(motorbike);
# Inserting in specific index
motorbike.insert(2,'tvs');
print(motorbike);
# Remove item
motorbike.remove('honda');
print(motorbike);
# pop item
motorbike.pop(3);
print(motorbike);
# clear
motorbike.clear();
print(motorbike);
